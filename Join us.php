    $(function() {

        var tabsSwiper = new Swiper('#about_tab', {
            speed: 500,
            spaceBetween: 20,
            onSlideChangeStart: function() {
                $(".about_nav .active").removeClass('active')
                $(".about_nav a").eq(tabsSwiper.activeIndex).addClass('active')
            }
        });
        $(".about_nav a").on('touchstart mousedown', function(e) {
            e.preventDefault()
            $(".about_nav .active").removeClass('active')
            $(this).addClass('active')
            tabsSwiper.slideTo($(this).index())
        });
        $(".about_nav a").click(function(e) {
            e.preventDefault()
        });


        var swiper = new Swiper('#list', {
            pagination: '.swiper-pagination',
            slidesPerView: 5,
            slidesPerColumn: 2,
            paginationClickable: true,
            spaceBetween: 30,
            nested: true
        });

        function _resize() {
            var w = $(window).width();

            if (w <= 700) {
                var swiper = new Swiper('#list', {
                    pagination: '.swiper-pagination',
                    slidesPerView: 1,
                    slidesPerColumn: 2,
                    paginationClickable: true,
                    spaceBetween: 30,
                    nested: true
                });
            } else if (w <= 991 && w >= 768) {
                var swiper = new Swiper('#list', {
                    pagination: '.swiper-pagination',
                    slidesPerView: 3,
                    slidesPerColumn: 2,
                    paginationClickable: true,
                    spaceBetween: 30,
                    nested: true
                });
            } else if (w >= 992 && w <= 1199) {
                var swiper = new Swiper('#list', {
                    pagination: '.swiper-pagination',
                    slidesPerView: 4,
                    slidesPerColumn: 2,
                    paginationClickable: true,
                    spaceBetween: 30,
                    nested: true
                });
            } else {
                var swiper = new Swiper('#list', {
                    pagination: '.swiper-pagination',
                    slidesPerView: 5,
                    slidesPerColumn: 2,
                    paginationClickable: true,
                    spaceBetween: 30,
                    nested: true
                });

            }
        }
        _resize();

        $(window).resize(function() {
            _resize();
        });

        $('.list').each(function(index, el) {
            $(el).hover(function() {
                $(this).addClass('hover');
            }, function() {
                $(this).removeClass('hover');
            });
        });


    });
</script>